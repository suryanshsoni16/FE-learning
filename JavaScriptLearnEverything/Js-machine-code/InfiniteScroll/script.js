let loadCount = 0;
let loadPerLoad = 20;

const contentContainer = document.querySelector(".container");
const loading = document.getElementById("loading");

function loadContent() {
  let start = loadCount * loadPerLoad;
  let end = start + loadPerLoad;

  if (start >= contentArray.length) return;

  loading.style.display = "block";

  setTimeout(() => {
    for (let i = start; i < end && i < contentArray.length; i++) {
      let contentItem = document.createElement("div");
      contentItem.classList.add("content-card");
      contentItem.textContent = `Item no. ${i + 1}: ${contentArray[i]}`;
      contentContainer.appendChild(contentItem);
    }

    loadCount++;
    loading.style.display = "none";
  }, 500);
}

loadContent();

window.addEventListener("scroll", () => {
  if (window.innerHeight + window.scrollY >= document.body.offsetHeight - 10) {
    loadContent();
  }
});
